SELECT
CASE
    WHEN PRICE >= 100000 THEN '100000'
    WHEN PRICE >= 90000 AND PRICE < 100000 THEN '90000'
    WHEN PRICE >= 80000 AND PRICE < 900000 THEN '80000'
    WHEN PRICE >= 70000 AND PRICE < 800000 THEN '70000'
    WHEN PRICE >= 60000 AND PRICE < 700000 THEN '60000'
    WHEN PRICE >= 50000 AND PRICE < 600000 THEN '50000'
    WHEN PRICE >= 40000 AND PRICE < 500000 THEN '40000'
    WHEN PRICE >= 30000 AND PRICE < 400000 THEN '30000'
    WHEN PRICE >= 20000 AND PRICE < 300000 THEN '20000'
    WHEN PRICE >= 10000 AND PRICE < 200000 THEN '10000'
    ELSE '0'
END AS PRICE_GROUP,
COUNT(*) AS PRODUCT
FROM PRODUCT
GROUP BY PRICE_GROUP
ORDER BY PRICE_GROUP;